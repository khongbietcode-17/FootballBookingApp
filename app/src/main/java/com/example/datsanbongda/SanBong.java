package com.example.datsanbongda;

public class SanBong {
    public String idSanBong, tenSan, tinh, thanhPhoHuyen, giaSan;

    public SanBong() { }

    public SanBong(String idSanBong, String tenSan, String tinh, String thanhPhoHuyen, String giaSan) {
        this.idSanBong = idSanBong;
        this.tenSan = tenSan;
        this.tinh = tinh;
        this.thanhPhoHuyen = thanhPhoHuyen;
        this.giaSan = giaSan;
    }
}